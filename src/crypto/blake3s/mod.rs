/// BLAKE3 hash (standard mode).
pub fn blake3s(input: &[u8]) -> [u8; 32] {
    *blake3::hash(input).as_bytes()
}

/// BLAKE3 keyed hash.
pub fn blake3s_keyed(key: &[u8; 32], input: &[u8]) -> [u8; 32] {
    *blake3::keyed_hash(key, input).as_bytes()
}

/// BLAKE3 key derivation.
pub fn blake3s_derive_key(context: &str, input: &[u8]) -> [u8; 32] {
    blake3::derive_key(context, input)
}

