use blake2::digest::consts::U32;
use blake2::digest::generic_array::GenericArray;
use blake2::{Blake2s256, Digest};

/// BLAKE2s-256 hash.
pub fn blake2s(input: &[u8]) -> [u8; 32] {
    let result: GenericArray<u8, U32> = Blake2s256::digest(input);
    result.into()
}

