use aes::cipher::generic_array::GenericArray;
use aes::cipher::{BlockDecryptMut, BlockEncryptMut, KeyIvInit};

type Aes128CbcEnc = cbc::Encryptor<aes::Aes128>;
type Aes128CbcDec = cbc::Decryptor<aes::Aes128>;

/// AES-128-CBC encrypt. Input length must be a multiple of 16 (no padding applied).
pub fn encrypt_buffer_cbc(plaintext: &[u8], iv: &[u8; 16], key: &[u8; 16]) -> Vec<u8> {
    assert!(
        plaintext.len() % 16 == 0,
        "plaintext length must be a multiple of 16"
    );
    let mut buf = plaintext.to_vec();
    let mut enc = Aes128CbcEnc::new(key.into(), iv.into());
    let blocks: &mut [GenericArray<u8, _>] = unsafe {
        // SAFETY: GenericArray<u8, U16> has the same layout as [u8; 16], and buf.len() is a
        // multiple of 16. We're reinterpreting a &mut [u8] as &mut [GenericArray<u8, U16>].
        let ptr = buf.as_mut_ptr() as *mut GenericArray<u8, _>;
        let n = buf.len() / 16;
        std::slice::from_raw_parts_mut(ptr, n)
    };
    enc.encrypt_blocks_mut(blocks);
    buf
}

/// AES-128-CBC decrypt. Input length must be a multiple of 16 (no padding applied).
pub fn decrypt_buffer_cbc(ciphertext: &[u8], iv: &[u8; 16], key: &[u8; 16]) -> Vec<u8> {
    assert!(
        ciphertext.len() % 16 == 0,
        "ciphertext length must be a multiple of 16"
    );
    let mut buf = ciphertext.to_vec();
    let mut dec = Aes128CbcDec::new(key.into(), iv.into());
    let blocks: &mut [GenericArray<u8, _>] = unsafe {
        // SAFETY: same as encrypt â€” GenericArray<u8, U16> is layout-compatible with [u8; 16].
        let ptr = buf.as_mut_ptr() as *mut GenericArray<u8, _>;
        let n = buf.len() / 16;
        std::slice::from_raw_parts_mut(ptr, n)
    };
    dec.decrypt_blocks_mut(blocks);
    buf
}

