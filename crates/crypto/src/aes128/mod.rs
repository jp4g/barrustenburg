    // BB test: single-block AES-128 cipher via CBC with zero IV (equivalent to ECB for one block)
    #[test]
    fn test_verify_cipher() {
        let key: [u8; 16] = [
            0x2b, 0x7e, 0x15, 0x16, 0x28, 0xae, 0xd2, 0xa6, 0xab, 0xf7, 0x15, 0x88, 0x09, 0xcf,
            0x4f, 0x3c,
        ];
        let plaintext: [u8; 16] = [
            0x6b, 0xc1, 0xbe, 0xe2, 0x2e, 0x40, 0x9f, 0x96, 0xe9, 0x3d, 0x7e, 0x11, 0x73, 0x93,
            0x17, 0x2a,
        ];
        let expected: [u8; 16] = [
            0x3a, 0xd7, 0x7b, 0xb4, 0x0d, 0x7a, 0x36, 0x60, 0xa8, 0x9e, 0xca, 0xf3, 0x24, 0x66,
            0xef, 0x97,
        ];

        // Single-block encrypt with zero IV is equivalent to ECB
        let iv = [0u8; 16];
        let result = encrypt_buffer_cbc(&plaintext, &iv, &key);
        assert_eq!(&result[..], &expected[..]);
    }

